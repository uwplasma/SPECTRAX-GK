#!/usr/bin/env python3
"""Build matched multi-sample nonlinear transport optimization campaigns.

The broad nonlinear turbulent-flux optimization claim needs more than one
baseline-to-candidate heat-flux trace.  This tool writes a reproducible matrix
campaign over surfaces, field-line labels, and ky values, then aggregates the
completed matched comparisons.  It writes launch and postprocessing commands;
it does not run simulations by itself.
"""

