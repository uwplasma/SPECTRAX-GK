def build_report_parser(subparsers: argparse._SubParsersAction[argparse.ArgumentParser]) -> None:
    parser = subparsers.add_parser("report", help="aggregate a completed matched nonlinear transport matrix")
    parser.add_argument("--matrix-manifest", required=True, type=Path)
    parser.add_argument("--out-json", required=True, type=Path)
    parser.add_argument("--out-figure", type=Path)
    parser.add_argument("--min-pass-fraction", type=float, default=1.0)
    parser.add_argument("--min-mean-relative-reduction", type=float, default=0.02)
    parser.add_argument("--fail-on-blocked", action="store_true")
    parser.set_defaults(func=_cmd_report)


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description=__doc__)
    subparsers = parser.add_subparsers(dest="command", required=True)
    build_write_parser(subparsers)
    build_report_parser(subparsers)
    return parser


def _cmd_write(args: argparse.Namespace) -> int:
    manifest = build_campaign(args)
    print(
        json.dumps(
            {
                "sample_count": manifest["config"]["sample_count"],
                "coverage_passed": manifest["coverage_gate"]["passed"],
                "manifest": _repo_relative(Path(args.out_dir) / "matched_transport_matrix_manifest.json"),
                "staged_script": manifest["launch_scripts"]["staged_ladder_skip_existing"],
                "final_horizon_script": manifest["launch_scripts"]["final_horizon_direct_skip_existing"],
                "postprocess_script": manifest["launch_scripts"]["postprocess"],
            },
            indent=2,
            sort_keys=True,
        )
    )
    return 0


def _cmd_report(args: argparse.Namespace) -> int:
    report = build_report(
        matrix_manifest=args.matrix_manifest,
        min_pass_fraction=float(args.min_pass_fraction),
        min_mean_relative_reduction=float(args.min_mean_relative_reduction),
    )
    args.out_json.parent.mkdir(parents=True, exist_ok=True)
    args.out_json.write_text(json.dumps(_json_clean(report), indent=2, sort_keys=True) + "\n", encoding="utf-8")
    if args.out_figure is not None:
        _write_report_figure(report, args.out_figure)
    print(json.dumps({"passed": report["passed"], "summary": report["summary"], "blockers": report["blockers"]}, indent=2, sort_keys=True))
    if args.fail_on_blocked and not bool(report["passed"]):
        return 1
    return 0


def main(argv: list[str] | None = None) -> int:
    args = build_parser().parse_args(argv)
    return int(args.func(args))


if __name__ == "__main__":  # pragma: no cover
    raise SystemExit(main())
